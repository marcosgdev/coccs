    <div class="row g-4">
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Dashboard</h3>
            <ol>
                <li>Acesse o menu <strong>Dashboard</strong> para ver indicadores gerais.</li>
                <li>Passe o mouse sobre cards e icones de informacao para entender a origem dos dados.</li>
                <li>Use os alertas de valores elevados para conferir importacoes suspeitas.</li>
            </ol>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Contratos</h3>
            <ol>
                <li>Use filtros por texto, ano, situacao e setor.</li>
                <li>Clique na chave do contrato para abrir o detalhe completo.</li>
                <li>Usuarios com permissao de escrita podem criar, editar, duplicar, marcar como estrategico e gerar notificacao.</li>
            </ol>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">ARPs / Atas</h3>
            <p>Consulte atas importadas ou cadastradas manualmente, seus fornecedores, vigencia, saldos e situacao calculada.</p>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Execucao financeira</h3>
            <p>Acompanhe exercicio, valor atualizado, valor executado, acumulado e saldo. Os dados normalmente chegam das abas financeiras da planilha.</p>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Aditivos</h3>
            <p>Registre ou consulte aditivos vinculados ao contrato, incluindo tipo, data, acrescimos, supressoes e nova data de termino.</p>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Prorrogacoes e prazos</h3>
            <p>Use a tela de prazos para priorizar vencidos, vencimentos em 30/60/90 dias, contratos indeterminados e prorrogacoes fora do prazo.</p>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Gestao e fiscalizacao</h3>
            <p>Confira carga por servidor, contratos sem gestor e contratos sem fiscal. Essa tela ajuda a balancear responsabilidades.</p>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Notificacoes</h3>
            <p>Consulte notificacoes geradas. Usuarios com permissao de escrita podem criar rascunhos e marcar notificacoes como enviadas.</p>
        </div>
        <div class="col-12">
            <h3 class="h6 fw-bold">Relatorios</h3>
            <p>Selecione o tipo de relatorio, aplique filtros quando existirem e exporte CSV quando precisar tratar os dados fora do sistema.</p>
        </div>
    </div>
</section>

<section class="gc-card p-4 mb-4" id="administrativa">
    <h2 class="h5 fw-bold">Parte administrativa</h2>
    <p class="text-secondary">A area administrativa e exibida apenas para o perfil Administrador. O usuario comum pode conhecer sua existencia, mas nao acessa as telas abaixo.</p>
    <div class="row g-4">
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Importacao</h3>
            <ol>
                <li>Envie a planilha `.xlsm` ou `.xlsx`.</li>
                <li>Gere a pre-visualizacao para conferir abas, cabecalhos e formulas.</li>
                <li>Simule quando quiser validar sem gravar dados.</li>
                <li>Importe quando a pre-visualizacao estiver consistente.</li>
                <li>Use <strong>Desfazer</strong> para desativar somente os dados daquele lote.</li>
            </ol>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Logs de importacao</h3>
            <p>Filtre por lote, tipo, status e aba para auditar simulacoes, importacoes validas e erros.</p>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Auditoria</h3>
            <p>Consulte eventos de criacao, edicao, exclusao logica e operacoes sensiveis.</p>
        </div>
        <div class="col-12 col-lg-6">
            <h3 class="h6 fw-bold">Cadastros auxiliares</h3>
            <p>Mantenha fornecedores, setores, naturezas, formas de contratacao, tipos, bases legais, unidades, modelos e perfis.</p>
        </div>
        <div class="col-12">
            <h3 class="h6 fw-bold">Configuracoes e usuarios</h3>
