        </div>
    </div>
</section>

<section class="gc-card p-4" id="boas-praticas">
    <h2 class="h5 fw-bold">Boas praticas de uso</h2>
    <ul>
        <li>Confira valores muito altos antes de usar indicadores financeiros em documentos oficiais.</li>
        <li>Use filtros para reduzir tabelas grandes antes de exportar.</li>
        <li>Prefira desfazer um lote de importacao quando encontrar erro de mapeamento.</li>
        <li>Nao compartilhe credenciais de administrador.</li>
        <li>Mantenha registros manuais consistentes com a planilha oficial.</li>
    </ul>
</section>
